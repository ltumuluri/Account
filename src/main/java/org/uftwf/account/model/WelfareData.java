package org.uftwf.account.model;

import org.uft.plunkit.Welfare;

/**
 * Created by xyang on 1/30/19.
 */
public class WelfareData {
    private String welfareStatus;
    private String entryDate;
    private String welfareId;
    public WelfareData(String welfareStatus,String entryDate,String welfareId){
        this.welfareId=welfareId;
        this.welfareStatus=welfareStatus;
        this.entryDate=entryDate;
    }

    public String getWelfareStatus() {
        return welfareStatus;
    }

    public void setWelfareStatus(String welfareStatus) {
        this.welfareStatus = welfareStatus;
    }

    public String getEntryDate() {
        return entryDate;
    }

    public void setEntryDate(String entryDate) {
        this.entryDate = entryDate;
    }

    public String getWelfareId() {
        return welfareId;
    }

    public void setWelfareId(String welfareId) {
        this.welfareId = welfareId;
    }
}
